export default [
  // 获取菜单列表
  {
    url: '/admin/acl/permission',
    method: 'get',
    response: () => {
      return {
        code: 200,
        data: menuList,
      }
    },
  },
  // 添加菜单
  {
    url: '/admin/acl/permission/save',
    method: 'post',
    response: ({ body }) => {
      const newMenu = {
        ...body,
        id: Date.now(),
        createTime: '2023-01-01 00:00:00',
        updateTime: '2023-01-01 00:00:00',
        toCode: null,
        status: null,
        select: false,
        children: body.level === 4 ? undefined : []
      }

      // 如果pid为0，添加到根菜单
      if (body.pid === 0) {
        menuList.push(newMenu)
      } else {
        // 递归查找父级并添加
        const findAndAdd = (list) => {
          for (let item of list) {
            if (item.id === body.pid) {
              if (!item.children) item.children = []
              item.children.push(newMenu)
              return true
            }
            if (item.children && findAndAdd(item.children)) {
              return true
            }
          }
          return false
        }
        findAndAdd(menuList)
      }

      return {
        code: 200,
        data: null,
        message: '添加成功',
      }
    },
  },
  // 更新菜单
  {
    url: '/admin/acl/permission/update',
    method: 'put',
    response: ({ body }) => {
      const updateMenu = (list) => {
        for (let i = 0; i < list.length; i++) {
          if (list[i].id === body.id) {
            list[i] = { ...list[i], ...body }
            return true
          }
          if (list[i].children && updateMenu(list[i].children)) {
            return true
          }
        }
        return false
      }
      updateMenu(menuList)
      return {
        code: 200,
        data: null,
        message: '更新成功',
      }
    },
  },
  // 删除菜单
  {
    url: '/admin/acl/permission/remove/:id',
    method: 'delete',
    response: (request) => {
      const id = Number(request.url.split('/').pop())
      const deleteMenu = (list) => {
        for (let i = 0; i < list.length; i++) {
          if (list[i].id === id) {
            list.splice(i, 1)
            return true
          }
          if (list[i].children && deleteMenu(list[i].children)) {
            return true
          }
        }
        return false
      }
      deleteMenu(menuList)
      return {
        code: 200,
        data: null,
        message: '删除成功',
      }
    },
  },
]